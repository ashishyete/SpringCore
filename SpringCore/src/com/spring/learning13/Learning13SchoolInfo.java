package com.spring.learning13;

/**
 * @author Ashish
 *
 */
public class Learning13SchoolInfo {

	private int schoolId;
	private String schoolName;
	private String schoolRanking;

	/**
	 * @return the schoolId
	 */
	public int getSchoolId() {
		return schoolId;
	}

	/**
	 * @param schoolId
	 *            the schoolId to set
	 */
	public void setSchoolId(int schoolId) {
		this.schoolId = schoolId;
	}

	/**
	 * @return the schoolName
	 */
	public String getSchoolName() {
		return schoolName;
	}

	/**
	 * @param schoolName
	 *            the schoolName to set
	 */
	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}

	/**
	 * @return the schoolRanking
	 */
	public String getSchoolRanking() {
		return schoolRanking;
	}

	/**
	 * @param schoolRanking
	 *            the schoolRanking to set
	 */
	public void setSchoolRanking(String schoolRanking) {
		this.schoolRanking = schoolRanking;
	}

}
